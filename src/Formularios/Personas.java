    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        Personas personas = new Personas();
        try {
            personas.setCod_Personas(Integer.parseInt(this.txt_cod_personas.getText()));

            int result = JOptionPane.showConfirmDialog(this, "Desea Eliminar el Registro", "Salir", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_NO_OPTION) {
                if (personas.eliminar_docentes(con)) {
                    llenarJtable();
                    btn_LimpiarActionPerformed(null);
                    JOptionPane.showMessageDialog(null, "El Registro se Elimino con Exito");
                } else {
                    JOptionPane.showMessageDialog(null, "Error , Fallo al Eliminar el Registro");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(PersonasForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        Docentes docentes = new Docentes();
        docentes.setCod_Docente(Integer.parseInt(this.txt_cod_docente.getText()));
        docentes.setDUI(this.txt_dui.getText());
        docentes.setPri_Nombre(this.txt_pri_nombre.getText());
        docentes.setSeg_Nombre(this.txt_seg_nombre.getText());
        docentes.setPri_Apellido(this.txt_pri_apellido.getText());
        docentes.setSeg_Apellido(this.txt_seg_apellido.getText());
        docentes.setGenero(this.cbx_genero.getSelectedItem().toString().charAt(0));
        docentes.setEstado_Civil(this.cbx_estado_civil.getSelectedItem().toString().charAt(0));
        docentes.setFecha_Nacimiento(this.dpk_FechaNacimiento.getDate());
        docentes.setTelefono_Particular(this.txt_telefono.getText());
       ;
        docentes.setDireccion(this.txt_direccion.getText());
      
        
        int result;
        result = JOptionPane.showConfirmDialog(this, "Desea Actualizar el Registro", "Salir", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_NO_OPTION) {
            if (docentes.actualizar_docentes(con)) {
                try {
                    llenarJtable();
                } catch (SQLException ex) {
                    Logger.getLogger(PersonasForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null, "Registro se ha guardado con exito");

            } else {
                JOptionPane.showMessageDialog(null, "Error , Fallo al Actualizo el Registro");
            }
        }
    }//GEN-LAST:event_btn_actualizarActionPerformed

